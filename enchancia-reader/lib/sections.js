export function getSectionUnlockStatus(sectionId) {
  const section = sections.find(s => s.id === sectionId);
  if (!section) return { unlocked: false, unlockTime: null };
  
  const now = Date.now();
  return {
    unlocked: now >= section.unlockTime,
    unlockTime: section.unlockTime
  };
}

export function getNextUnlock() {
  const now = Date.now();
  const nextSection = sections.find(s => s.unlockTime > now);
  return nextSection ? { id: nextSection.id, unlockTime: nextSection.unlockTime } : null;
}
